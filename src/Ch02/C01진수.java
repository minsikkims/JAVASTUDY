package Ch02;

public class C01진수 {

	public static void main(String[] args) {
		// 01 진수 개념
		// 02 10->2진수 변환
		// 03 2->10진수 변환
		// 04 문제
		//1		1	1	1	1	1	1	1
		//128	64	32	16	8	4	2	1

		//----------------------------------------------------------
		//문제  2진수 -> 10진수
		//----------------------------------------------------------
		//10000100(2) 	->		(10)
		//11010100(2) 	->		(10)
		//10011100(2) 	-> 		(10)
		//01011000(2) 	-> 		(10)
		//10011100(2) 	-> 		(10)
		//
		//
		//----------------------------------------------------------
		//문제  8진수 -> 10진수
		//----------------------------------------------------------
		//75(8)		->	00111101	32+16+8+4+1	(10)
		//45(8)		->	00100101	32+4+1		(10)
		//112(8)	->	01001010	64+8+2		(10)
		//15(8)		->	00001101	8+4+1		(10)
		//67(8)		->			(10)
		//55(8)		->			(10)
		//
		//----------------------------------------------------------
		//문제  16진수 -> 10진수
		//----------------------------------------------------------
		//9A(16)		->	10011010	(10)
		//16(16)		->	00010110    (10)
		//55(16)		->				(10)
		//FF(16)		->				(10)
		//D9(16)		->	11011001 	(10)
		//3B(16)		->				(10)
		
//		7    5
//		111  101
//		00111101 = 32 + 16 + 8 + 4 + 1
//				=61
//
//
//		4     5
//		100   101
//		00100101 = 32 + 4 + 1 = 37
//
//
//		9A
//		10011010 = 128 + 16 + 8 + 2

		
		//10진수로 변환
		System.out.printf("10진수 : %d\n",0b10101101);	//0b	: 2진수
		System.out.printf("10진수 : %d\n", 173);			//		: 10진수
		System.out.printf("10진수 : %d\n", 0255);			//0 	: 8진수
		System.out.printf("10진수 : %d\n", 0xAD);			//0x 	: 16진수
		System.out.println();
		//8진수로 변환
		System.out.printf("08진수 : %o\n",0b10101101);	//0b	: 2진수
		System.out.printf("08진수 : %o\n", 173);			//		: 10진수
		System.out.printf("08진수 : %o\n", 0255);			//0 	: 8진수
		System.out.printf("08진수 : %o\n", 0xAD);			//0x 	: 16진수
		System.out.println();
		//16진수로 변환
		System.out.printf("16진수 : %x\n",0b10101101);	//0b	: 2진수
		System.out.printf("16진수 : %x\n", 173);			//		: 10진수
		System.out.printf("16진수 : %X\n", 0255);			//0 	: 8진수
		System.out.printf("16진수 : %X\n", 0xAD);			//0x 	: 16진수
		System.out.println();
	 
	}

}
